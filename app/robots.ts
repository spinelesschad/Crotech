import type { MetadataRoute } from "next";

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: "*",
        allow: "/",
        disallow: ["/api", "/_next", "/admin"],
      },
      {
        userAgent: "GPTBot",
        allow: "/",
        disallow: ["/api", "/_next", "/admin"],
      },
    ],
    sitemap: "https://crotech.io/sitemap.xml",
  };
}
